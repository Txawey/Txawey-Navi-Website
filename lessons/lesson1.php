    <h4>Tricky Sounds</h4>
    <h5>Ejectives</h5>
    <p>
      Let\'s start with the most fun ones: KxeKx, PxePx, and TxeTx! Ejectives involve releasing a
      large gust of air which is built up by holding your mouth in the position
      which the respective sound is made. So with PxePx, for example, you
      hold your lips pursed as though you are going to say the letter p
      and build up air pressure behind your lips. Then, release that gust
      of air all at once. There is no voicing involved with the ejectives;
      all of the sound should come from the release of the air. In the end,
      the ejectives sound a lot like beatboxing, so you can use that idea as
      a guide.
    </p>
    <h5>Unaspirated</h5>
    <p>
      The unaspirated consonants are the polar opposite of the ejectives:
      KeK, PeP, and TeT. In stark contrast to how the ejectives\' sounds are made,
      the unaspirated consonants are voiced sounds that have little to no air release.
      Most English speakers pronounce k, p, and t with a voiced release of air;
      a good way to test this is to hold your hand in front of your mouth: if you
      feel a gust of air, that is the aspiration in your pronunciation. For Na\'vi, you
      will want to try to make the same mouth movement but without the air gust. Another
      way to see how close you are is to listen to the sounds you make: KeK should sound halfway
      between k and g, PeP should sound halfway between p and b, and TeT should sound
      halfway between t and d.
    </p>
    <h5>Various Others</h5>
    <p> A few other weird sounds are tìftang, NgeNg, ReR, and \'Rr.</p>
    <p> tìftang is type of sound called a glottal stop. For English speakers,
      think of the break in the middle of the word "uh-oh." That is the glottal
      stop. This is a crucial part of the name of our language here: Na\'vi is pronounced
      [Naʔ.vi] not as [Na.vi]. </p>
    <p> ReR and \'Rr are what are known as alveolar consonants. ReR is an Alveolar Tap; for
      English speakers the best way to think about this sound is to say the word "butter" somewhat
      fast. Typically, especially for Midwestern US English, the two t\'s in the middle of the
      word join into an Alveolar Tap. What is happening is your tongue is swiftly flicking
      the top of your mouth. For \'Rr, the position of the tongue is similar but the sound
      is slightly different, as this is an Alveolar Trill. You may know it as the Spanish or
      Italian rolled r. This is the sound that has taken me the longest to grasp, and I still
      cannot completely do it. Usually I can get one if I\'m having fun and am relaxed, but it is
      still inconsistent for me. I have built a document based on my struggles with this and
      based on some correspondence I had with an Italian professor; <a href="https://docs.google.com/document/d/148KELZsno9RxbQhptoNnH__4J4BBlHaMh5TTX5mp534/edit?usp=sharing">here</a> is the document.</p>
    <p> NgeNg is one that can be trickier than you might think. The way I think about it is: if
      you look at what position your mouth is in when you say m or n, it is in the same position as
      the letters p and t respectively. The NgeNg sound is made in the same position as the letter k.
      Instead of the sound style you use with k, p, or t, you make the more nasal sound of m or n, but in
      the k position. You should be able to make this sound with your mouth completely wide open.</p>

